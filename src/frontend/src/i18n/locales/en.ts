// Loose<T>: same structure as T, but every leaf string literal widens to `string`
// (and readonly arrays widen to mutable). Lets EN keep an identical shape to IT
// without requiring identical values.
type Loose<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? Loose<U>[]
    : { [K in keyof T]: Loose<T[K]> };

